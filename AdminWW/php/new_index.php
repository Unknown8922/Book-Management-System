    Founded by law no. 14 in 2016 during the national Year of Reading , the library is the vision of His Highness Sheikh Mohammed bin Rashid Al Maktoum. Supporting Dubai and UAE goals to develop a knowledge society, the creation of the Mohammed bin Rashid Library follows a number of important initiatives set in motion to promote and encourage reading, literacy and Arabic literature.<br></br>

    Launched in 2014 our library's primary goal is to stimulate a passion for knowledge among all those living in the United Arab Emirates, in particular among young people. We actively support and encourage reading, research, creativity and entrepreneurship by providing free access to an outstanding range of books and other knowledge resources, quality information services and our event calendar.</p>
